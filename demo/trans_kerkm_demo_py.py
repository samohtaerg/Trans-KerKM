def gaussian_kernel(X1, X2, sigma):
    """Compute Gaussian (RBF) kernel matrix.

    Parameters
    ----------
    X1 : ndarray of shape (n1, d)
    X2 : ndarray of shape (n2, d)
    sigma : float
        Bandwidth parameter. Larger sigma = wider neighborhood.

    Returns
    -------
    K : ndarray of shape (n1, n2)
        K[i, j] = exp(-||X1[i] - X2[j]||^2 / (2 * sigma^2))
    """
    dists = cdist(X1, X2, metric='sqeuclidean')
    return np.exp(-dists / (2 * sigma ** 2))


def theta_from_target_by_group_mean(h0, beta, x_mean, target=0.10):
    """Compute exponential censoring rate to achieve a target censoring fraction.

    Given a Cox subgroup with baseline hazard h0 and coefficient vector beta,
    the subgroup event rate is lambda_g = h0 * exp(beta · x_mean).
    We calibrate the censoring rate theta_g so that
        P(C < T | G = g) = target,
    which gives theta_g = (target / (1 - target)) * lambda_g.

    Parameters
    ----------
    h0 : float
        Subgroup-specific baseline hazard.
    beta : array-like of shape (d,)
        Subgroup-specific coefficient vector.
    x_mean : array-like of shape (d,)
        Subgroup mean covariate vector used for calibration.
    target : float
        Desired censoring fraction (default 0.10).

    Returns
    -------
    lam : float
        Subgroup event rate lambda_g = h0 * exp(beta · x_mean).
    theta : float
        Exponential censoring rate theta_g.
    """
    beta = np.asarray(beta, dtype=float)
    x_mean = np.asarray(x_mean, dtype=float)
    lam = float(h0 * np.exp(beta @ x_mean))
    theta = float((target / (1.0 - target)) * lam)
    return lam, theta


def compute_individualized_hazard(x_i, X_train, Y_train, delta_train,
                                  X_source, Y_source, delta_source,
                                  sigma, lambda_value, apply_loo=True):
    """Estimate the discrete-time hazard function for a single target point x_i.

    Implements Equations (3.1)-(3.3) of the paper. For each observed event
    time t_l, the kernel-weighted event count D^{(-i)}(t_l | x_i) and
    kernel-weighted risk set R^{(-i)}(t_{l-1} | x_i) are computed by
    aggregating over target observations (excluding x_i via LOO) and source
    observations down-weighted by 1/lambda.

    Parameters
    ----------
    x_i : ndarray of shape (d,)
        Covariate vector of the query point.
    X_train : ndarray of shape (n, d)
        Target training covariates.
    Y_train : ndarray of shape (n,)
        Target observed times Y = min(T, C).
    delta_train : ndarray of shape (n,)
        Target event indicators delta = 1{T <= C}.
    X_source : ndarray of shape (m, d)
        Source covariates.
    Y_source : ndarray of shape (m,)
        Source observed times.
    delta_source : ndarray of shape (m,)
        Source event indicators.
    sigma : float
        Kernel bandwidth.
    lambda_value : float
        Global source down-weighting parameter (lambda >= 1).
        lambda = 1: naive pooling. lambda -> inf: target-only.
    apply_loo : bool
        If True, exclude x_i from its own risk and event counts (LOO correction).

    Returns
    -------
    unique_times : ndarray
        Sorted union of all observed event times from target and source.
    hazard_values : ndarray
        Estimated discrete hazard h_hat(t_l | x_i) at each time point.
    """
    x_i = np.atleast_2d(x_i)
    X_train = np.atleast_2d(X_train)
    X_source = np.atleast_2d(X_source)

    K_target = gaussian_kernel(x_i, X_train, sigma).flatten()
    K_source = gaussian_kernel(x_i, X_source, sigma).flatten()

    if apply_loo:
        self_idx = np.where(K_target > 0.999)[0]
        if len(self_idx) > 0:
            K_target[self_idx] = 0

    unique_times = np.sort(np.unique(np.concatenate([Y_train, Y_source])))
    hazard_values = []

    for l, t_l in enumerate(unique_times):
        t_prev = 0 if l == 0 else unique_times[l - 1]

        # Numerator: kernel-weighted event counts (Eq. 3.1)
        target_events_sum = np.sum(K_target * (delta_train == 1) * (Y_train == t_l))
        source_events_sum = np.sum(K_source * (delta_source == 1) * (Y_source == t_l))
        numerator = target_events_sum + source_events_sum / lambda_value

        # Denominator: kernel-weighted risk set (Eq. 3.2)
        target_at_risk_sum = np.sum(K_target * (Y_train > t_prev))
        source_at_risk_sum = np.sum(K_source * (Y_source > t_prev))
        denominator = target_at_risk_sum + source_at_risk_sum / lambda_value

        hazard_values.append(numerator / denominator if denominator > 0 else 0)

    return unique_times, np.array(hazard_values)


def compute_survival_function(hazard_times, hazard_values):
    """Convert discrete hazard estimates to a survival function.

    Implements the product-limit update of Eq. (3.4)-(3.5):
        S_hat(t_l | x_i) = S_hat(t_{l-1} | x_i) * (1 - h_hat(t_l | x_i))
    with S_hat(t_0 | x_i) = 1.

    Parameters
    ----------
    hazard_times : ndarray of shape (L,)
        Ordered event times t_1, ..., t_L.
    hazard_values : ndarray of shape (L,)
        Estimated hazard h_hat(t_l | x_i) at each time point.

    Returns
    -------
    times : ndarray of shape (L+1,)
        Time grid with t_0 = 0 prepended.
    survival_probs : ndarray of shape (L+1,)
        Estimated survival probabilities S_hat(t | x_i).
    """
    n_times = len(hazard_times)
    survival_probs = np.ones(n_times + 1)
    times = np.concatenate(([0], hazard_times))

    for i in range(n_times):
        survival_probs[i + 1] = survival_probs[i] * (1 - hazard_values[i])

    survival_probs = np.clip(survival_probs, 0, 1)
    return times, survival_probs


def compute_c_index_from_survival_curves(survival_curves, Y_true, delta):
    """Compute C-index from a collection of individualized survival curves.

    Each curve is summarized by its expected survival time M_i (Eq. 3.6),
    and the risk score is R_i = -M_i (Eq. 3.7) so that higher risk
    corresponds to shorter predicted survival.

    Parameters
    ----------
    survival_curves : dict
        Keys are integer sample indices. Values are (times, probs) tuples.
    Y_true : ndarray of shape (n,)
        Observed times.
    delta : ndarray of shape (n,)
        Event indicators.

    Returns
    -------
    c_index : float
        Harrell's C-index (Eq. 3.8).
    """
    n_samples = len(Y_true)
    risk_scores = np.zeros(n_samples)

    for i in range(n_samples):
        if i not in survival_curves:
            continue
        times, probs = survival_curves[i]
        time_diffs = np.diff(times)
        avg_probs = (probs[:-1] + probs[1:]) / 2
        risk_scores[i] = np.sum(avg_probs * time_diffs)

    return concordance_index(Y_true, risk_scores, delta)


def grid_search_cv(X_train, Y_train, delta_train,
                   X_source, Y_source, delta_source,
                   sigma_grid, lambda_grid,
                   n_folds=5, apply_loo=True, random_state=None):
    """K-fold cross-validated grid search over (sigma, lambda).

    For each candidate pair, fits Trans-KerKM on the training folds and
    evaluates C-index on the held-out fold. Returns the pair with the
    highest mean validation C-index.

    Parameters
    ----------
    X_train, Y_train, delta_train : target training data.
    X_source, Y_source, delta_source : source data (fixed across folds).
    sigma_grid : list of float
        Candidate bandwidth values sigma.
    lambda_grid : list of float
        Candidate source down-weighting values lambda.
    n_folds : int
        Number of CV folds.
    apply_loo : bool
        Whether to apply LOO correction inside the hazard estimator.
    random_state : int or None

    Returns
    -------
    best_sigma : float
    best_lambda : float
    best_c_index : float
        Mean validation C-index at the best (sigma, lambda) pair.
    """
    best_sigma, best_lambda, best_c_index = None, None, 0.0
    kf = KFold(n_splits=n_folds, shuffle=True, random_state=random_state)

    for sigma in sigma_grid:
        for lambda_value in lambda_grid:
            fold_scores = []

            for train_idx, val_idx in kf.split(X_train):
                X_tr = X_train[train_idx]
                Y_tr = Y_train[train_idx]
                d_tr = delta_train[train_idx]
                X_val = X_train[val_idx]
                Y_val = Y_train[val_idx]
                d_val = delta_train[val_idx]

                curves = {}
                for i, x_i in enumerate(X_val):
                    ht, hv = compute_individualized_hazard(
                        x_i, X_tr, Y_tr, d_tr,
                        X_source, Y_source, delta_source,
                        sigma, lambda_value, apply_loo=apply_loo
                    )
                    times, probs = compute_survival_function(ht, hv)
                    curves[i] = (times, probs)

                fold_scores.append(
                    compute_c_index_from_survival_curves(curves, Y_val, d_val)
                )

            mean_score = np.mean(fold_scores)
            print(f"  sigma={sigma:.4f}, lambda={lambda_value:.4f}: "
                  f"CV C-index = {mean_score:.4f}")

            if mean_score > best_c_index:
                best_c_index = mean_score
                best_sigma = sigma
                best_lambda = lambda_value

    print(f"\nBest: sigma={best_sigma:.4f}, lambda={best_lambda:.4f}, "
          f"C-index={best_c_index:.4f}")
    return best_sigma, best_lambda, best_c_index


def kernel_weighted_transfer_km(X_source, Y_source, delta_source,
                                X_train_target, Y_train_target, delta_train_target,
                                X_test_target, Y_test_target, delta_test_target,
                                sigma_grid=None, lambda_grid=None,
                                n_folds=5, apply_loo=True, random_state=None):
    """Trans-KerKM estimator with cross-validated parameter selection.

    Standardizes features using training statistics, selects (sigma*, lambda*)
    by K-fold CV on the target training set, fits the final model on the full
    training set, and evaluates on the test set.

    Note
    ----
    The np.repeat data-augmentation block present in the original HPC script
    has been removed. That block repeated target training rows 10x before CV,
    which violated the LOO guarantee and is not part of the estimator as
    defined in the paper.

    Parameters
    ----------
    X_source, Y_source, delta_source : source domain data.
    X_train_target, Y_train_target, delta_train_target : target training data.
    X_test_target, Y_test_target, delta_test_target : target test data.
    sigma_grid : list of float or None
        If None, uses 5-point log-scale grid [0.1, ..., 10.0].
    lambda_grid : list of float or None
        If None, uses 5-point log-scale grid [0.1, ..., 10.0].
    n_folds : int
    apply_loo : bool
    random_state : int or None

    Returns
    -------
    best_params : dict with keys 'sigma' and 'lambda'.
    final_model : None (placeholder for API consistency with HPC script).
    test_c_index : float
    """
    np.random.seed(random_state)

    scaler = StandardScaler()
    X_all = np.vstack([X_source, X_train_target]) if X_source.shape[0] > 0 else X_train_target
    scaler.fit(X_all)

    X_source_s = scaler.transform(X_source) if X_source.shape[0] > 0 else X_source
    X_train_s = scaler.transform(X_train_target)
    X_test_s = scaler.transform(X_test_target)

    # NOTE: np.repeat block removed (see docstring above)

    if sigma_grid is None:
        sigma_grid = np.logspace(np.log10(0.1), np.log10(10.0), 5).tolist()
    if lambda_grid is None:
        lambda_grid = np.logspace(np.log10(0.1), np.log10(10.0), 5).tolist()

    print(f"  sigma_grid  : {[f'{s:.4f}' for s in sigma_grid]}")
    print(f"  lambda_grid : {[f'{l:.4f}' for l in lambda_grid]}")
    print(f"  CV folds    : {n_folds}")

    best_sigma, best_lambda, _ = grid_search_cv(
        X_train_s, Y_train_target, delta_train_target,
        X_source_s, Y_source, delta_source,
        sigma_grid, lambda_grid,
        n_folds=n_folds, apply_loo=apply_loo, random_state=random_state
    )

    test_curves = {}
    for i, x_i in enumerate(X_test_s):
        ht, hv = compute_individualized_hazard(
            x_i, X_train_s, Y_train_target, delta_train_target,
            X_source_s, Y_source, delta_source,
            best_sigma, best_lambda, apply_loo=apply_loo
        )
        times, probs = compute_survival_function(ht, hv)
        test_curves[i] = (times, probs)

    test_c_index = compute_c_index_from_survival_curves(
        test_curves, Y_test_target, delta_test_target
    )
    print(f"Test set C-index: {test_c_index:.4f}")

    best_params = {'sigma': best_sigma, 'lambda': best_lambda}
    final_model = None  # placeholder for API consistency

    return best_params, final_model, test_c_index


