def fit_cox_model(X_train, Y_train, delta_train,
                  X_test, Y_test, delta_test, col_names=None):
    """Fit a target-only Cox proportional hazards model.

    Parameters
    ----------
    X_train, Y_train, delta_train : target training data.
    X_test, Y_test, delta_test : test data.
    col_names : list of str or None

    Returns
    -------
    cph : CoxPHFitter
    c_index : float
    """
    if col_names is None:
        col_names = [f"X{i+1}" for i in range(X_train.shape[1])]

    train_df = pd.DataFrame(X_train, columns=col_names)
    train_df['time'] = Y_train
    train_df['event'] = delta_train

    cph = CoxPHFitter()
    cph.fit(train_df, duration_col='time', event_col='event')

    test_df = pd.DataFrame(X_test, columns=col_names)
    risk_scores = cph.predict_partial_hazard(test_df)
    c_index = concordance_index(Y_test, -risk_scores, delta_test)

    return cph, c_index


def fit_transfer_cox_model(X_source, Y_source, delta_source,
                           X_train_target, Y_train_target, delta_train_target,
                           X_test_target, Y_test_target, delta_test_target,
                           random_state=None, col_names=None):
    """Fit a naive pooling Cox model (source + target training data combined).

    Parameters
    ----------
    X_source, Y_source, delta_source : source data.
    X_train_target, Y_train_target, delta_train_target : target training data.
    X_test_target, Y_test_target, delta_test_target : test data.
    random_state : int or None (unused, kept for API consistency)
    col_names : list of str or None

    Returns
    -------
    cph : CoxPHFitter
    c_index : float
    X_test_target, Y_test_target, delta_test_target : passed through for
        API consistency with the HPC script.
    """
    if col_names is None:
        col_names = [f"X{i+1}" for i in range(X_source.shape[1])]

    source_df = pd.DataFrame(X_source, columns=col_names)
    source_df['time'] = Y_source
    source_df['event'] = delta_source

    target_df = pd.DataFrame(X_train_target, columns=col_names)
    target_df['time'] = Y_train_target
    target_df['event'] = delta_train_target

    combined_df = pd.concat([source_df, target_df])

    cph = CoxPHFitter()
    cph.fit(combined_df, duration_col='time', event_col='event')

    test_df = pd.DataFrame(X_test_target, columns=col_names)
    risk_scores = cph.predict_partial_hazard(test_df)
    c_index = concordance_index(Y_test_target, -risk_scores, delta_test_target)

    return cph, c_index, X_test_target, Y_test_target, delta_test_target


